        <p>
          El picaflor de Arica emite un canto muy suave que evoca el sonido de
          las cigarras. Ancestralmente, vivió en los valles del sur del Perú
          donde actualmente está extinto, mientras en el Norte de Chile su
          situación actual es crítica.
        </p>
        <p>
          Durante cuatro décadas, esta especie pasó de ser apreciada como el
          picaflor más común de los valles del norte de Chile a ser el más
          escaso, por lo cual fue declarado “en peligro crítico de extinción”.
          Desde la primera estimación que se hizo de su población en el 2003,
          esta comunidad ha reducido su tamaño un en 83,4 %.
        </p>
        <p>
          Por su amplitud y gran abundancia de flores, hasta el año 2008 Azapa
          albergaba la mayor población del picaflor de Arica. Sin embargo, gran
          cantidad de su territorio ha sido quemado, transformado en zonas de
          cultivos, viveros o cubierto con basura y otros desechos, perdiendo
          drásticamente su hábitat.
        </p>
        <p>
          La presencia de árboles es muy importante para la existencia de la
          especie. La mayoría prefiere habitar zonas que tienen una superficie
          de 10 a 30 % cubierta de árboles porque, especialmente los
          frutales, cumplen un rol importante en su proceso de reproducción.
          Además del arbolado, otro elemento relevante para esta ave es la
          presencia de flores productoras de néctar.
        </p>
        <p>
          Este es el principal componente de la dieta y debido al pequeño tamaño
          de su pico, el picaflor de Arica tiende a visitar flores más
          apropiadas para insectos que para aves. Se alimenta en vuelo haciendo
          visitas muy cortas a cada flor (probablemente debido al bajo contenido
          de néctar de la mayoría de ellas). Pero también se ha observado
          alimentándose posado sobre las flores, lo que le permite ahorrar
          energía.
        </p>
        <p>
          Este alimento, si bien es muy importante, no contiene todos los
          nutrientes necesarios para el desarrollo de esta ave. Así que las
          proteínas son obtenidas de pequeños insectos que cazan al vuelo. Al
          analizar fecas de polluelos se detectaron presas pertenecientes a
          arácnidos, coleópteros, mosquitos y larvas en general.
        </p>
        <p>
          Aparentemente durante el período posterior a la reproducción, un porcentaje
          importante de la población del picaflor de Arica se desplaza a la zona
          de la prepuna donde aprovechan la floración producida por las lluvias
          de verano. Aunque la utilización de esta flora no ha sido evaluada
          directamente, considerando que existe un alto número de especies
          potencialmente útiles para el picaflor de Arica. Entre estas destacan
          numerosas Fabaceae de los géneros Adesmia, Astragalus y Lupinus,
          Cactaceae del género Opuntia, Solanaceae (Solanum) y algunas
          Asteraceae como Muticia, Chuquiraga, Senecio, etc.
        </p>
        <h2 class="pb-3">El bosque y la reproducción</h2>
        <p>
          Mientras que los territorios de los machos suelen estar asociados a la
          presencia de alimento principalmente chañar, los sitios de
          nidificación están presentes en áreas con árboles y desprovistas de
          flores atractivas para las aves.
        </p>
        <p>
          Los machos son altamente territoriales aunque no defienden un sector
          reproductivo propiamente tal. Durante la época de nidificación los
          machos realizan un despliegue de posesión de su espacio formando una
          “U” entre las perchas. Ascienden rápidamente desde una y se detienen
          por algunos segundos a unos 8-12 m sobre el suelo, posteriormente
          bajar y vuelve a subir en el otro extremo de la “U”. Este
          comportamiento se puede repetir varias veces.
        </p>
        <p>
          Este tipo de despliegue probablemente tiene un rol más importante para
          la competencia entre machos que como cortejo para las hembras. El
          único cortejo que se ha observado, involucra una serie de movimientos
          laterales muy rápidos del macho con las plumas de la garganta erizadas
          y enfrentando a una hembra perchada dentro de un arbusto.
        </p>
        <p>
          Los machos no participan en el proceso de cuidado de las crías, son
          las hembras las encargadas de construir el nido, lo crean formando una
          copa para lo que puede usar lana de ovejas, plumas, fibras vegetales y
          telas de araña.
        </p>
        <p>
          En un estudio realizado en Chaca entre 2006 y 2008, la mayoría de los
          nidos fueron encontrados en un área de no más de 2 hectáreas de
          olivos. Por otro lado, la ausencia de nidos en árboles de cítricos
          (10% del área) indica que el picaflor de Arica puede estar evitando
          estos árboles para nidificar.
        </p>
        <p>
          De un total de 76 nidos estudiados, la mayoría de ellos (86 %) fueron
          observados en olivos y unos pocos (12 %) en árboles de mango. Únicamente dos
          nidos han sido observados en los árboles nativos sauce amargo (Salix
          humboldtiana) en Codpa y pimiento molle (Schinus molle) en Camarones y
          uno en una brea (Tessaria absinthioides) en Chaca. Con esta evidencia
          se abren nuevas oportunidades para reforestar con otras especies que
          le sean útiles al picaflor de Arica.
        </p>
        <p>
          El beneficio de los olivares como sitio de nidificación para esta
          especie es compartido positivamente por diversas aves de los valles
          del norte de Chile. Una explicación potencial es que los olivos pueden
          haber reemplazado a formaciones boscosas desaparecidas hace tiempo
          producto de la actividad del hombre.
        </p>
        <p>
          Durante los años de estudio en Chaca, se registró una densidad de 9,3
          a 14,3 nidos activos por hectárea. La altísima concentración de
          nidos probablemente se asocia a una falta de comportamiento
          territorial de las hembras. De hecho, en algunos casos se encontraron
          algunos nidos simultáneamente activos a unos pocos metros unos de
          otros. Tampoco se observaron comportamientos de defensa del nido, en
          contraste con las evidentes agresiones manifestadas por las hembras en
          torno a los sitios de alimentación.
        </p>
        <p>
          El período de incubación varía entre 16 a 19 días y la duración del
          período de polluelos fluctúa de 27 a 35 días. Aparentemente, las crías
          son capaces de abandonar el nido varios días antes, pero “prefieren”
          quedarse por más tiempo. Así, se estima que la duración del período de
          reproducción (sin considerar la duración de la construcción del nido)
          es aproximadamente de 48 días, lo que representa un período muy largo
          para el común de los picaflores. Este factor junto con el corto
          período de floración de las principales especies consumidas por el
          picaflor de Arica sugieren que son pocas las posibilidades que las
          hembras puedan tener dos nidadas.
        </p>
        <p>
          Un intento de revertir su inminente extinción es la creación del
          Monumento Natural Picaflor de Arica. Ubicada a 50 km al sur de Arica,
          en el valle de Chaca, el monumento es la primera micro-reserva de una
          red de sitios dentro de la Región de Arica y Parinacota con la que se
          busca la recuperación del ave. En sus 10,8 hectáreas de superficie,
          resguarda también vegetación propia del bosque espinoso tropical
          interior, compuesta por el chañar (Geoffroea decorticans) y el
          algarrobo (Prosopis alba), cuyos relictos no contaban con protección.
        </p>
        
